#include <iostream>

int main() {
    // initialise the first integer
    int num1{8};

    // initialise the second integer
    int num2{10};

    // compute the sum of the two values
    int total = num1 + num2;

    // print the computed total to the console
    std::cout << total << std::endl;

    // terminate programme
    return 0;
}